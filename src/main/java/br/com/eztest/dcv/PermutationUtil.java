    /**
     * Constroi uma tabela de permutacoes generica para uma lista de elementos. Cada linha eh uma configuracao de
     * permutacao. E em cada celula da tabela, esta o indice da lista original para a permutacao em questao.
     * 
     * OBS: todas as listas sao pre-calculadas e armazenadas como constantes. Sao permitidos calculos de tabelas de 0 a
     * 6 elementos apenas.
     * 
     * @param permutationSize
     *            tamanho da lista de elementos que se deseja permutar.
     * @return tabela de permutacao para uma lista de permutationSize elementos.
     */
    public static int[][] getPermutationIndexes(final int permutationSize) {
        final int[][] original = PermutationUtil.PERMUTATIONS[permutationSize];
        final int[][] ret = new int[PermutationUtil.PERMUTATIONS[permutationSize].length][];
        for (int i = 0; i < original.length; i++) {
            ret[i] = Arrays.copyOf(original[i], original[i].length);
        }
        return ret;
    }

